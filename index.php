    <h1>9. The main deserts</h1>

    <div class="around">

        <a id="image" href="https://upload.wikimedia.org/wikipedia/commons/e/e2/Deserts.png" title="Largest non-polar deserts. Source: Wikipedia."> <img class="fitImage" src="https://upload.wikimedia.org/wikipedia/commons/thumb/e/e2/Deserts.png/800px-Deserts.png" alt="" /></a>

        <p><i><span class="centerText">Largest non-polar deserts. Source: Wikipedia.</span></i></p>

    </div>

    <p>Deserts are dry areas due to receiving low amounts of precipitation - be it from rain, snow, mist or fog. There are four classifications of deserts: hot, cold, semiarid and coastal.</p>

    <p>The understanding of the function of water in the environment is important to explain hot deserts. As explained in an earlier chapter, water functions as a carrier of temperature in nature. In rainforests, nights tend not to be much colder than days for this reason. In hot deserts, because there is little cloud cover during the day, days are hotter than in others environments. Come night, all this heat dissipates for lack of humidity, and it becomes much colder. This great variation of temperature in one day-cycle further degrades rocks. Because this condition has been present for a long time, it has had lasting effect upon the soil and the presence of life. Animals and plants that can survive with little humidity might be present. A topical presence of water form micro-environments, but it cannot have effect over large areas. This is the case of oases and passing rivers.</p>

    <p>Cold deserts are dry because the air is too cold, so there is no evaporation of snow cover. The little precipitation they get fall in the form os snow and accumulates with the snow cover in a similar way to sand. The snow forms dunes and blizzards like sand dunes and sans storms in arid deserts.</p>

    <p>Semiarid deserts form in the transition between deserts and other biomes, as the humidity rises. As we have seen in the biomes chapter, there are many different kinds. They may have short rainy seasons that are not enough to change the landscape from a desert to another biome.</p>

    <p>Coastal deserts are sometimes long and thin, following along a coast and a mountain range. As explained before, the mountains force clouds upwards, causing them to rain, but only on one side. Besides losing humidity, these air currents also lose heat as they move upwards. For this reason, these deserts are also colder and have lower temperature variation in the day-cycle.</p>

