    <h3>Glaciers</h3>

    <p>Speaking of glaciers, you can see one in the comic. They are accumulations of ice on top of mountains due to the cold weather on high altitudes. The cold weather is a result of less atmospheric pressure, which leads to less air, meaning less matter to gather heat from the Sun. Glaciers are massive and kept in constant movement due to the weight of ever accumulating snow. They partially melt in the warm months, giving rise to rivers that increase discharge in the summer.</p>

