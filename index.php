    </div>

    <p>The most famous are stalactites (on the ceiling) and stalagmites (on the floor). When connected, these form a column. The image below shows many more types of speleothems. Head on over to the <a href="https://en.wikipedia.org/wiki/Speleothem">Wikipedia article on speleothems</a> to see it further explained.</p>

    <div class="around">

        <a id="image" href="https://upload.wikimedia.org/wikipedia/commons/6/61/Tropfsteine.svg" title="Speleothems. Source: Wikipedia."> <img class="fitImage" src="https://upload.wikimedia.org/wikipedia/commons/thumb/6/61/Tropfsteine.svg/800px-Tropfsteine.svg.png" alt="" /></a>

