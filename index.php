    <h2>The Caspian Sea and the Aral Sea</h2>

    <div class="around">

        <a id="image" href="https://upload.wikimedia.org/wikipedia/commons/8/8d/Caspianseamap.png" title="The Caspian Sea and the Aral Sea. Source: Wikipedia."> <img class="fitImage" src="https://upload.wikimedia.org/wikipedia/commons/8/8d/Caspianseamap.png" alt="" /></a>

