    <h1>2. <em>Homo sapiens sapiens</em> and their original habitat</h1>

    <p>The evolutionary history of humans starts 7 million years ago. First we have the sahelanthropus, for 3 million years, then the australopithecus, for another 2 million years. Both lived in Africa and are not considered humans. The “Anatomically Modern Human”, or <em>Homo sapiens sapiens</em>, first appears around 200,000 years ago. The second “sapiens” differentiates modern humans from the <em>Homo sapiens idaltu</em>. This does not include neanderthals, which were either <em>Homo sapiens neanderthals</em> or <em>Homo neanderthals</em>, if a different species. The <em>Homo</em> genus had yet more species, all now extinct, and the neanderthals became extinct 40,000 years ago.</p>

    <p>Humans could not have evolved from the monkey, since they are both modern species. The species we did evolve from, however, looked like this:</p>

