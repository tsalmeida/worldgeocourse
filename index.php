    <p>In the Mediterranean we find scrubby but dense vegetation, broad-leaves shrubs, brushes and small trees. These are characteristic of hot, dry summers and mild, wet winters. Most rainfall happens during winters. There are summer storms, but in general it is subject to many dry months. Mediterranean plant life does not shed leaves before winter - meaning, they are “evergreen”.</p>

