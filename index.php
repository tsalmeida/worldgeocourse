    </div>

    <h3>Solutional caves</h3>

    <p>Solutional caves are the most common, formed in soluble rocks over time, dissolved by the natural acidity in water. A few soluble rock types are: limestone (most common), chalk, dolomite, marble, salt and gypsium. As a result of the water seeping in from above, these caves feature speleothems, special formations of rock. The image below shows the most common speleothems.</p>

    <div class="around">

        <a id="image" href="https://upload.wikimedia.org/wikipedia/commons/a/a1/Labeled_speleothems.jpg" title="The most common types of speleothems. Source: Wikipedia."> <img class="fitImage" src="https://upload.wikimedia.org/wikipedia/commons/thumb/a/a1/Labeled_speleothems.jpg/800px-Labeled_speleothems.jpg" alt="" /></a>

