    <p>The first thing to notice is that some are converging—moving closer—and others diverging—moving apart.</p>

    <h2>Diverging tectonic plates</h2>

    <p>If you search the map for diverging tectonic plates, you will see most diverging areas are under the sea, except for Iceland. The geology of Iceland is unique that way and, as a result, it contains more than 30 active volcanoes and geysers.</p>
    <p>Now you may ask, what makes tectonic plates move? The answer is in this divergence area, something there is pushing the tectonic plates away. The name of the force responsible is "seafloor spreading". Unseen by most people and only recently discovered by scientists, it is one of the most important engines of the planet.</p>
    <p>The solidification of the magma erupted from the exposed area between tectonic plates continually creates volcanoes and seafloor. As this process continues, new seafloor pushes away old seafloor and the tectonic plates themselves, as seen below.</p>

    <div class="around">

        <a id="image" href="https://upload.wikimedia.org/wikipedia/commons/c/c0/Mid-ocean_ridge_topography.gif" title="Seafloor spreading. Source: Wikipedia."> <img class="fitImage" src="https://upload.wikimedia.org/wikipedia/commons/c/c0/Mid-ocean_ridge_topography.gif" alt="" /></a>

        <p><i><span class="centerText">Seafloor spreading. Source: Wikipedia.</span></i></p>

