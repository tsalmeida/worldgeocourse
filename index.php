    <h3>Rivers</h3>

    <p>We move on to rivers. There are two sources for free-flowing surface water: melting glaciers and water springs. Both are present in the comic. Water springs do not have a constant discharge, varying according to rainfall. The rainwater then flows through the ground and becomes trapped in cave systems. Some may dry up completely in the low-rainfall season. Hot water springs also exist, with water pumped from underground by volcanic activity. Below you will see an example of a water spring.</p>

    <div class="around">

        <a id="image" href="http://upload.wikimedia.org/wikipedia/commons/f/f6/Nacentemackinac.jpg" title="A natural water spring from the state of Michigan, United States. Source: Wikipedia."> <img class="fitImage" src="http://upload.wikimedia.org/wikipedia/commons/thumb/f/f6/Nacentemackinac.jpg/450px-Nacentemackinac.jpg" alt="" /></a>

