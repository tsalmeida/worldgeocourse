    <h2>Converging tectonic plates</h2>

    <p>When tectonic plates converge, either of two things can happen: one climbs over the other or both rise, pushing against each other. As a result, mountain chains form on the convergence zones over million of years.</p>

    <p>A mountain is a tall landform, usually in the form of a peak, set apart from hills both by their height and by being steeper. Though some mountains stand isolated, most are part of mountain ranges. There are three types: volcanic, fold and block.</p>

    <h2>Volcanic Mountains</h2>

    <div class="around">

        <a id="image" href="http://www.bgs.ac.uk/discoveringgeology/hazards/volcanoes/images/tectonics_fig2.png" title="Volcanic formation. Source: British Geological Survey."> <img class="fitImage" src="http://www.bgs.ac.uk/discoveringgeology/hazards/volcanoes/images/tectonics_fig2.png" alt="" /></a>

        <p><i><span class="centerText">Volcanic formation. Source: <a href="http://www.bgs.ac.uk/discoveringGeology/hazards/volcanoes/home.html">British Geological Survey</a>.</span></i></p>

    </div>

    <p>Volcanic mountains are or have been volcanoes, the result of one tectonic plate sliding over another. When this happens, the magma from under the rising plate comes out to the surface and solidifies, which forms the volcano. Now we see the main types of volcanoes.</p>

    <h3>Stratovolcanoes</h3>

    <p>Where magma eruptions are pre-hardened, they don’t spread over a large area, but instead gather in a cone shape, resulting in stratovolcanoes. They can also be called “composite volcanoes”, as different eruptions pile over each other.</p>

    <h3>Shield volcanoes</h3>

    <p>However, where the magma is more liquid, it tends to be expelled continually for long periods of time and to go further before solidification. The resulting volcano is spread out over hundreds of kilometers and looks like a shield lay flat on the floor.</p>

    <h3>Cinder cone volcanoes</h3>

    <p>Other eruption events are more sudden, erupting in an explosion of lava fountains and volcanic ash under pressure from volcanic gas. The material of the resulting volcano is less compact than found on the previous types, mostly loose rocks of solidified lava. They are cone-shaped and often have a dome on the peak.</p>

    <h2>Fold mountains</h2>

    <p>When two tectonic plates collide, the clashing section of the plates rise and become fold mountains. The material of fold mountains is hard rock, since that’s what we find under the surface. These mountains are still rising after earthquakes.</p>

    <div class="around">

        <a id="image" href="http://geographylwc.org.uk/images/5tect/fold2.jpg" title="Fold mountains formation. Source: http://geographylwc.org.uk/"> <img class="fitImage" src="http://geographylwc.org.uk/images/5tect/fold2.jpg" alt="" /></a>

        <p><i><span class="centerText">Fold mountains formation. Source: <a href="http://geographylwc.org.uk/GCSE/Year5/5tect/fold%20mts.html">http://geographylwc.org.uk/</a></span></i></p>

    </div>

    <h2>Block mountains</h2>

    <p>It is well known that there are faults between the planet’s tectonic plates, but there are faults inside them as well. The bedrock of a tectonic plate is not even, but is instead broken up into fault blocks, some as large as continents. Some fault blocks move vertically in relation to other neighboring fault blocks. Be it upwards or downwards movement, block mountains are the resulting height difference.</p>

