    </div>

    <p>You might not have a correct impression of a rainforest from watching movies. Traversing a rainforest is difficult, as plants stand close to each other. Dead leaves and other plant material, as well as roots, cover the soil. The temperature is high, there is no wind. You can’t see too far ahead because of all the plants blocking your view. To pass through a virgin rainforest, you need to carry some kind of blade and hack through as you go. They cover some of the hardest areas to explore in the planet, reason why they are sparsely populated and largely unknown.</p>

