    <p>Due to interactions between land and sea, the winds do not follow the general path outlined in the previous map. Below is the “real” wind map I mentioned before.</p>

    <div class="around">

        <a id="image" href="https://upload.wikimedia.org/wikipedia/commons/1/18/Map_prevailing_winds_on_earth.png" title="Winds world map. Source: Wikipedia."> <img class="fitImage" src="https://upload.wikimedia.org/wikipedia/commons/thumb/1/18/Map_prevailing_winds_on_earth.png/800px-Map_prevailing_winds_on_earth.png" alt="" /></a>

