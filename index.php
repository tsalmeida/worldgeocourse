    <p>So all that happens in the atmosphere, but what about the oceans? After all, ocean heat is also uneven based on distance to the Equator. In a similar way to Atmospheric Circulation, we have Ocean Circulation, resulting in the Thermohaline circulation, or the “global conveyor belt”.</p>

    <p>They are important for the weather in many ways, but especially because warm currents help warm up surface winds.</p>

