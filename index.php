    <h2>Polar Desert</h2>

    <div class="around">

        <a id="image" href="https://upload.wikimedia.org/wikipedia/commons/b/bd/AntarcticaDomeCSnow.jpg" title="Polar Desert. Source: Wikipedia."> <img class="fitImage" src="https://upload.wikimedia.org/wikipedia/commons/thumb/b/bd/AntarcticaDomeCSnow.jpg/800px-AntarcticaDomeCSnow.jpg" alt="" /></a>

        <p><i><span class="centerText">Polar Desert. Source: Wikipedia.</span></i></p>

    </div>

    <p>Polar deserts have low precipitation and a mean temperature, during summer, of less than 10°C. We find polar deserts near Earth’s North and South Pole. Antarctica is not included in the Vegetation Map above, but most of its area would fall under this classification.</p>

    <h2>Tundra</h2>

    <div class="around">

        <a id="image" href="https://upload.wikimedia.org/wikipedia/commons/8/87/Tundra_in_Siberia.jpg" title="Tundra. Source: Wikipedia."> <img class="fitImage" src="https://upload.wikimedia.org/wikipedia/commons/thumb/8/87/Tundra_in_Siberia.jpg/800px-Tundra_in_Siberia.jpg" alt="" /></a>

        <p><i><span class="centerText">Tundra. Source: Wikipedia.</span></i></p>

    </div>

    <p>Tundra forms in areas where the extreme cold and short summers do not allow for tree growth. Unlike in polar deserts, in Tundras you will find small plants such as shrub, grass and moss, as well as lichen. This stark environment does not allow an abundance of animal life, but birds do migrate to tundras for water. There are three types of tundra: Arctic, Alpine and Antarctica. Alpine Tundra forms where high altitudes create an environment akin to the polar environment.</p>

    <h2>Taiga</h2>

    <div class="around">

        <a id="image" href="https://upload.wikimedia.org/wikipedia/commons/2/2d/Picea_glauca_taiga.jpg" title="Taiga. Source: Wikipedia."> <img class="fitImage" src="https://upload.wikimedia.org/wikipedia/commons/2/2d/Picea_glauca_taiga.jpg" alt="" /></a>

        <p><i><span class="centerText">Taiga in Alaska, US. Source: Wikipedia.</span></i></p>

    </div>

    <p>As we move further south from the poles, conifers are the first kind of trees we will find. Taigas are the zone marked by their presence alone among trees. Their leaves are long and thin, some are scale-like, and they are not shed before winter. Conservation of energy is key to survive with little sunlight. Conifers’ leaves do not provide enough nutrients for most animals, but taigas do have more animal life than tundras.</p>

    <h2>Temperate Broadleaf Forest</h2>

    <div class="around">

        <a id="image" href="https://upload.wikimedia.org/wikipedia/commons/9/9e/Diqing%2C_Yunnan%2C_China.jpg" title="Temperate Broadleaf Forest. Source: Wikipedia."> <img class="fitImage" src="https://upload.wikimedia.org/wikipedia/commons/9/9e/Diqing%2C_Yunnan%2C_China.jpg" alt="" /></a>

        <p><i><span class="centerText">Temperate Broadleaf Forest. Source: Wikipedia.</span></i></p>

    </div>

    <p>Moving further south, as days and summers become longer, it is important for trees to be able to capture the extra sunlight. For this, they need broad leaves, unlike the hard, needle-like conifer leaves. But these broad leaves cannot withstand winter. For this reason, deciduous trees suck back the nutrients from the leaves, then shed them. This is why they lose the green color. Animal life becomes richer as leaves are more edible.</p>

    <h2>Temperate Steppe</h2>

    <div class="around">

        <a id="image" href="https://upload.wikimedia.org/wikipedia/commons/9/91/Steppe_of_western_Kazakhstan_in_the_early_spring.jpg" title="Temperate Steppe in Kazakhstan. Source: Wikipedia."> <img class="fitImage" src="https://upload.wikimedia.org/wikipedia/commons/thumb/9/91/Steppe_of_western_Kazakhstan_in_the_early_spring.jpg/800px-Steppe_of_western_Kazakhstan_in_the_early_spring.jpg" alt="" /></a>

        <p><i><span class="centerText">Temperate Steppe in Kazakhstan. Source: Wikipedia.</span></i></p>

    </div>

    <p>Going further south, we find Steppe where there isn’t enough water to sustain trees. In these areas we find grasses and shrubs. Temperature variation is great. Between summer and winter, because of proximity to the poles. Between day and night, because low air humidity doesn’t allow for heat retention.</p>

    <h2>Subtropical Rainforest</h2>

    <div class="around">

        <a id="image" href="https://upload.wikimedia.org/wikipedia/commons/e/e3/Juniper_Springs_Ocala_National_Forest.jpg" title="Subtropical Rainforest in Florida, US. Source: Wikipedia."> <img class="fitImage" src="https://upload.wikimedia.org/wikipedia/commons/e/e3/Juniper_Springs_Ocala_National_Forest.jpg" alt="" /></a>

        <p><i><span class="centerText">Subtropical Rainforest in Florida, US. Source: Wikipedia.</span></i></p>

    </div>

    <p>Subtropical rainforests enjoy larger precipitation than the previous biomes. Limitations such as short summers and low sunlight are also absent. This allows for more richness of both plant and animal life. Many of the species found on subtropical rainforests are the same found in tropical rainforests. The difference is that less precipitation and lower temperatures make their presence more sparse.</p>

    <h2>Mediterranean Vegetation</h2>

    <div class="around">

        <a id="image" href="https://upload.wikimedia.org/wikipedia/commons/2/28/Dehesa_Los_Valhondos_Jamon_BEHER_Bernardo_Hernandez_Salamanca_Pelayos.jpg" title="Mediterranean Forest in Spain. Source: Wikipedia."> <img class="fitImage" src="https://upload.wikimedia.org/wikipedia/commons/thumb/2/28/Dehesa_Los_Valhondos_Jamon_BEHER_Bernardo_Hernandez_Salamanca_Pelayos.jpg/800px-Dehesa_Los_Valhondos_Jamon_BEHER_Bernardo_Hernandez_Salamanca_Pelayos.jpg" alt="" /></a>

