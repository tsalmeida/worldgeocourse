    <p>In blue, you have the westerlies. In yellow, the northeasterly trade winds. In orange, the southeasterly trade winds. The regions marked with the green and pink numbers are low-pressure areas. We will explain more about those later.</p>

    <p>To understand the addition of land into this process, this is the most important fact: land warms faster than water, but also cools faster. As a result, in the warmer months land area will be warmer than the ocean. However, in the cold months the ocean keeps the heat it gets for longer, while land loses it faster. As a result, the ocean will be warmer than the land.</p>

    <h3>Northeast and Southeast trade winds</h3>

    <p>The northeast and southeast trade winds are cold surface winds moving from the Poles to the Equator. Both flow east to west due to the Coriolis Effect.</p>

    <p>The Northeast trade winds allowed European expansion into the Americas. In a similar way, they carry hurricanes to the Caribbean and North America and cyclones to Asia.</p>

    <h3>Westerlies</h3>

    <p>The Westerlies, or anti-trades, blow in the opposite direction of the trade winds. During winter, when the polar winds are strongest, the Westerlies also increase in strength.</p>

    <h3>The Coriolis Effect</h3>

    <p>Now why do the Westerlies flow East, while the trade winds flow West? As we have seen before, without the Coriolis Effect winds would blow in a straight line, either North or South. Here’s the difference: the trade winds are escaping from the Equator, blowing to the poles. However, the Westerlies are escaping from the poles, blowing toward the Equator. Considering this, here is how it works due to the coriolis effect:</p>

    <ul>
        <li><b>Northern Hemisphere:</b></li>
        <ul>
            <li>Moving <b>north to south</b>: winds <b>turn west</b> (northern hemisphere trade winds);</li>
            <li>Moving <b>south to north</b>: winds <b>turn east</b> (northern hemisphere westerlies);</li>
        </ul>
        <li><b>Southern Hemisphere:</b></li>
        <ul>
            <li>Moving <b>north to south</b>: winds <b>turn east</b> (southern hemisphere westerlies);</li>
            <li>Moving <b>south to north</b>: winds <b>turn west</b> (southern hemisphere trade winds);</li>
        </ul>
    </ul>

    <h3>Monsoon</h3>

    <p>The Monsoon winds affects the region surrounding the Indian Ocean: West Africa, South-East Asia and Australia. You may remember Monsoon Forests were one kind of biome we studied before. That biome shows one dry season and one wet season, when under the effect of monsoon rains, which the monsoon winds cause.</p>

    <p>The different heating rates between land and water come in to play to explain the monsoons. During summer, the Indian Ocean is colder than the land surrounding it. As a result, the air over land rises and is replaced by cool and wet air from the ocean. Upon reaching land, however, these winds are forced upward, which cools them further, releasing the humidity as rain. Those are the monsoon rains. This colder air then returns to the ocean, since it tends downward, like a waterfall from high land to low ocean.</p>

    <p>This whole process happens in a similar manner to generate coastal winds all over the world, but it is more intense in the monsoon region. Slight differences happen throughout Asia, but this explanation is detailed enough for our purposes.</p>

    <h3>Jet stream</h3>

    <p>For all the talk about the interaction between land and water and its effect on winds patterns, there is one type that is not affected. Jet streams are so high up, between the troposphere and the stratosphere, that land and sea cover does not factor into its functioning.</p>

    <p>Jet streams are fast and narrow air currents. They flow roughly between the three cells of atmospheric circulation: along the tropics and along the arctic and antarctic circles.</p>

    <div class="around">

        <a id="image" href="https://upload.wikimedia.org/wikipedia/commons/6/6b/Jetstreamconfig.jpg" title="Jet streams. Source: Wikipedia."> <img class="fitImage" src="https://upload.wikimedia.org/wikipedia/commons/6/6b/Jetstreamconfig.jpg" alt="" /></a>

        <p><i><span class="centerText">Jet stream. Source: Wikipedia.</span></i></p>

    </div>

    <p>Their practical importance lies in shortening travel times for airplanes that fly high enough to meet them. They are important for the climate because they steer storms. For example, in both 2007 and 2012 intense storms hit Britain due to changes in the polar jet stream in the Northern Hemisphere.</p>

    <h2>Atmospheric Pressure</h2>

    <p>Atmospheric pressure is the pressure exerted on everything on the surface by the weight of the Atmosphere. It is not easily apparent, but if you close an empty bottle on a mountain top, where the Atmosphere around you is rare and light, and then bring the bottle to the ocean level, the heavier atmosphere there will crush that bottle.</p>

    <p>Other than mountaintops, we also find low atmospheric pressure on deserts. That’s because the air warm and moving upward, so it doesn’t exert much force downward.</p>

    <p>High pressure areas are more important to us. Think back at the three atmospheric circulation cells, and how other than surface winds, they also had winds in the upper troposphere. Wherever you have cold winds coming down from two different atmospheric circulation cells in a same area, the pressure will be higher than in other places. In the same way, wherever you have warm wind going upward from two bordering cells, you have low pressure. Please take a look at the atmospheric circulation map with the green planet and find the blue H’s and the red L’s. Those are high and low atmospheric pressure regions.</p>

    <p>So, what happens there? First of all, in low-pressure areas the winds are stronger, since warm air, because it is lighter, moves faster than cold air. It also rains, since the hot air moving upward carries humidity, which then rains down as the air cools.</p>

    <p>Meanwhile, in high-pressure areas you have clear skies, as the air is dry. The lack of cloud cover also exposes the land to direct sunlight, but since there are no clouds to accumulate the heat, temperatures are lower.</p>

    <p>All would be fine if it were not for the Coriolis Effect. Again, because the Earth is spinning, all this motion tends to happen in a circular pattern. This is the result:</p>

    <div class="around">

        <a id="image" href="https://upload.wikimedia.org/wikipedia/commons/4/4f/Hurricane-en.svg" title="Structure of a Hurricane (in the Northern Hemisphere). Source: Wikipedia."> <img class="fitImage" src="https://upload.wikimedia.org/wikipedia/commons/thumb/4/4f/Hurricane-en.svg/800px-Hurricane-en.svg.png" alt="" /></a>

        <p><i><span class="centerText">Structure of a Hurricane (in the Northern Hemisphere). Source: Wikipedia.</span></i></p>

    </div>

    <p>Winds are stronger where the temperature difference that causes them to exist is larger. In low-atmospheric pressure areas, this difference can be very large and be created very fast because all that rising, wet and warm air cools off fast. As long as there’s warm water to keep the process going, it doesn’t stop. That’s why hurricanes damage coastal cities, but doesn’t go much farther. Once the hurricane is over land, it lacks fuel—the warm and wet winds—to keep it going.</p>

