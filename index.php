    <h1>14. Caves</h1>

    <p>There are several types of caves, but we will review only the most important. In general, a cave would be an area under the surface that is large enough for parts of it not to receive sunlight. Some are openings into rock, others are large areas under the ground big enough for a person to explore. Let's go through the types and see a few examples of each.</p>

    <h2>Cave types</h2>

    <h3>Shallow caves</h3>

    <p>Let's start with a few types of small and shallow caves.</p>

    <h4>Rock shelters</h4>

    <p>Rock shelters are useful as protection against the weather. They were often used by pre-historic men, making some important archelogical sites.</p>

    <div class="around">

        <a id="image" href="https://upload.wikimedia.org/wikipedia/commons/3/3f/Rock_shelter_Papula.jpg" title="Rock shelter in Europe. Source: Wikipedia."> <img class="fitImage" src="https://upload.wikimedia.org/wikipedia/commons/3/3f/Rock_shelter_Papula.jpg" alt="" /></a>

        <p><i><span class="centerText">Rock shelter in Europe. Source: <a href="https://en.wikipedia.org/wiki/File:Rock_shelter_Papula.jpg">Wikipedia</a>.</span></i></p>

    </div>

    <h4>Grottos</h4>

    <p>Another name for shallow caves, grottos can be either man-made or natural. Since the apparition of Our Lady of Lourdes, in France, 1858, they have been used for Catholic shrines.</p>

    <div class="around">

        <a id="image" href="https://www.flickr.com/photos/kecko/16758062773/" title="Catholic shrine in a grotto, Switzerland. Source: Flickr. Photographer: Kecko."> <img class="fitImage" src="https://farm8.staticflickr.com/7713/16758062773_8dff17ac46_b.jpg" alt="" /></a>

