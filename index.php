    <h3>Ice cap, highlands, non-permanent ice, tundra and the subarctic</h3>

    <p>In Greenland and Antarctica, we have “Ice cap”, meaning that those areas are permanently covered in ice. There’s an important distinction to be made here. “Ice sheets” or “continental glaciers” are ice covers with an area greater than 50,000Km2. There are two of them in the planet: on Greenland and on Antarctica. We call “ice caps” anything smaller, from mountaintop glaciers to the frozen Arctic. The ice cap climate region, which is not the same as an ice cap in general, covers both ice caps and ice sheets.</p>

    <p>This is confusing because we find ice caps on the “highlands” climate region. These are some of the tallest mountain chains we studied before, you should be able to identify them.</p>

    <p>In addition to this, and keeping with the "ice" theme, we have the non-permanent ice areas. This is a sort of alternate to the "ice cap" classification.</p>

    <p>The subarctic and tundra regions are cold due to proximity to the Arctic. As we have seen, they are covered by the tundra and taiga biomes.</p>

    <h3>Humid continental and Humid subtropical</h3>

    <p>Humid continental areas feature the classic four seasons system, shielded from a greater influence of rain-carrying winds by their inner placing.</p>

    <p>In contrast, Humid subtropical areas are typically on eastern coasts, receiving rainfall from westerlies winds.</p>

    <h3>Marine west coast and Mediterranean</h3>

    <p>Ever heard that in London or Seattle it rains a lot? That’s the marine west coast climate, constantly affected by rain-carrying westerlies winds (which, you must remember, travel east to west). These are the same rains affected by rainshadow, and you can see the resulting deserts in the map.</p>

    <p>The Mediterranean climate, just like the biome, shows hot, dry summers and mild, wet winters. </p>

    <h3>Arid and Semiarid</h3>

    <p>We are well familiarized with this condition from the biomes chapter. These areas receive very low amounts of rain.</p>

    <h3>Tropical wet and Tropical wet and dry</h3>

    <p>We also know both of these from their respective biomes. In tropical wet areas we see tropical rainforests. In tropical wet and dry, tropical and subtropical dry forests. We distinguish them by the amount of rain they receive.</p>

