    <h2>Xeric Shrublands</h2>

    <div class="around">

        <a id="image" href="https://upload.wikimedia.org/wikipedia/commons/d/d5/2013-07-04_15_37_14_Sagebrush-steppe_along_U.S._Route_93_in_central_Elko_County_in_Nevada.jpg" title="Xeric Shrublands in Arizona, US. Source: Wikipedia."> <img class="fitImage" src="https://upload.wikimedia.org/wikipedia/commons/thumb/d/d5/2013-07-04_15_37_14_Sagebrush-steppe_along_U.S._Route_93_in_central_Elko_County_in_Nevada.jpg/800px-2013-07-04_15_37_14_Sagebrush-steppe_along_U.S._Route_93_in_central_Elko_County_in_Nevada.jpg" alt="" /></a>

        <p><i><span class="centerText">Xeric Shrublands in Arizona, US. Source: Wikipedia.</span></i></p>

    </div>

    <p>As we move away from deserts into more moisture-rich regions, plant life begins to return. Xeric Shrublands are this transition zone. It supports tough and small vegetation, shrubs, which are able to survive with low humidity. Some animal life is present, such as reptiles. Xeric Shrublands are fragile and turn desertic with disruption.</p>

    <h2>Dry Steppe</h2>

    <div class="around">

        <a id="image" href="https://upload.wikimedia.org/wikipedia/commons/f/fa/Patagonian_plains_argentina.jpg" title="Dry Steppe in Patagonia, Argentina. Source: Wikipedia."> <img class="fitImage" src="https://upload.wikimedia.org/wikipedia/commons/thumb/f/fa/Patagonian_plains_argentina.jpg/800px-Patagonian_plains_argentina.jpg" alt="" /></a>

        <p><i><span class="centerText">Dry Steppe in Patagonia, Argentina. Source: Wikipedia.</span></i></p>

    </div>

    <p>In a past section we saw Temperate Steppe. The Dry Steppe is similar, but drier, closer to a desert. In practice, it is difficult to distinguish between the two kinds and even between them and Xeric Shrublands. You will find all three referred to simply as steppes, for example.</p>

    <h2>Semi-Arid Desert</h2>

    <div class="around">

        <a id="image" href="https://upload.wikimedia.org/wikipedia/commons/0/04/The_semi-arid_terrain_of_Machakos_%285112045372%29.jpg" title="Semi-Arid Desert in Machakos, Kenya. Source: Wikipedia."> <img class="fitImage" src="https://upload.wikimedia.org/wikipedia/commons/thumb/0/04/The_semi-arid_terrain_of_Machakos_%285112045372%29.jpg/800px-The_semi-arid_terrain_of_Machakos_%285112045372%29.jpg" alt="" /></a>

        <p><i><span class="centerText">Semi-Arid Desert in Machakos, Kenya. Source: Wikipedia.</span></i></p>

    </div>

    <p>Also referred to as steppe sometimes. Semi-deserts are mild deserts, with less temperature variation and higher humidity. For this reason, the soil is not as degraded as explained before. They form in the transition between deserts and more humid areas.</p>

    <h2>Grass Savanna</h2>

    <div class="around">

        <a id="image" href="https://upload.wikimedia.org/wikipedia/commons/1/11/Savanna_towards_the_south-east_from_the_south-west_of_Taita_Hills_Game_Lodge_within_the_Taita_Hills_Wildlife_Sanctuary_in_Kenya.jpg" title="Grass Savanna in Taita Hills, Kenya. Source: Wikipedia."> <img class="fitImage" src="https://upload.wikimedia.org/wikipedia/commons/thumb/1/11/Savanna_towards_the_south-east_from_the_south-west_of_Taita_Hills_Game_Lodge_within_the_Taita_Hills_Wildlife_Sanctuary_in_Kenya.jpg/800px-Savanna_towards_the_south-east_from_the_south-west_of_Taita_Hills_Game_Lodge_within_the_Taita_Hills_Wildlife_Sanctuary_in_Kenya.jpg" alt="" /></a>

        <p><i><span class="centerText">Grass Savanna in Taita Hills, Kenya. Source: Wikipedia.</span></i></p>

    </div>

    <p>We find Savannas in regions where the soil is able to sustain forests, but the climate is not humid enough. For this reason, they are fluid and expand or contract according to the conditions found. For example, deforestation of the Amazon Forest in Brazil gives place to “cerrado”, a type of savanna. This deforestation might be man-made, but it may also caused by natural climate fluctuation. A strong rainy season might expand the Amazon Forest and contract the cerrado, for example.</p>

    <h2>Tree Savanna</h2>

    <div class="around">

        <a id="image" href="https://upload.wikimedia.org/wikipedia/commons/3/37/Australian_savanna.jpg" title="Tree Savanna in Australia. Source: Wikipedia."> <img class="fitImage" src="https://upload.wikimedia.org/wikipedia/commons/3/37/Australian_savanna.jpg" alt="" /></a>

        <p><i><span class="centerText">Tree Savanna in Australia. Source: Wikipedia.</span></i></p>

    </div>

    <p>Tree Savannas are of course alike grass savannas, the difference being the presence of trees. This can follow from soil profile. The afore-mentioned cerrado itself has two types: <i>cerrado strictu-sensu</i> and <i>cerrado dense</i>. The latter is closer to a grass savanna, while the second is closer to a tree savanna. Though savannas are more open spaces than trees, the tree numbers might be similar. The difference is, higher humidity in forests allow trees to be larger, wider, and as a result their canopy cover a larger area.</p>

    <h2>Subtropical Dry Forest</h2>

    <div class="around">

        <a id="image" href="https://upload.wikimedia.org/wikipedia/commons/7/78/Chacachacare_dry_forest_3.JPG" title="Subtropical Dry Forest in Trinidad and Tobago (Caribbean). Source: Wikipedia."> <img class="fitImage" src="https://upload.wikimedia.org/wikipedia/commons/7/78/Chacachacare_dry_forest_3.JPG" alt="" /></a>

        <p><i><span class="centerText">Subtropical Dry Forest in Trinidad and Tobago (Caribbean). Source: Wikipedia.</span></i></p>

    </div>

    <p>Tropical Dry Forests populate areas where, though having a strong rainy season, also sport long dry seasons. Some tree species will shed leaves before the dry season, if they do not have a nearby source of water. As the trees shed leaves, they allow the sunlight to reach the ground, causing grass to grow. This would not happen in a Tropical Rainforest such as the Amazon, where barely any light reaches the soil.</p>

    <h2>Tropical Rainforest</h2>

    <div class="around">

        <a id="image" href="https://upload.wikimedia.org/wikipedia/commons/7/73/Amazon_CIAT_%282%29.jpg" title="Amazon Tropical Rainforest in Brazil. Source: Wikipedia."> <img class="fitImage" src="https://upload.wikimedia.org/wikipedia/commons/thumb/7/73/Amazon_CIAT_%282%29.jpg/800px-Amazon_CIAT_%282%29.jpg" alt="" /></a>

        <p><i><span class="centerText">Amazon Tropical Rainforest in Brazil. Source: Wikipedia.</span></i></p>

    </div>

    <p>Around the Equator, sunlight is abundant and always present. If precipitation is also supplied year-round, a tropical rainforest forms. There is no need for any shedding of leaves, as there is no dry nor cold season. The climate is always hot, always wet. Plant cover is overwhelming, only around 2% of sunlight reaches the soil. See the photo below for an example.</p>

    <div class="around">

        <a id="image" href="https://upload.wikimedia.org/wikipedia/commons/3/36/FRIM_canopy.JPG" title="Canopy of Rainforest in Malaysia. Source: Wikipedia."> <img class="fitImage" src="https://upload.wikimedia.org/wikipedia/commons/thumb/3/36/FRIM_canopy.JPG/800px-FRIM_canopy.JPG" alt="" /></a>

