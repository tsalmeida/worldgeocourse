    <p>A forest is a large area covered with trees or other woody vegetation. They make up for 26% of the world’s land area and contain 80% of the planet’s plant biomass. Different kinds of forest form in different parts of the world for a variety of reasons. In general, there are different kinds when nearer the poles, near the tropics and near the Equator. There are also variations according to altitude. Their size can vary with climate variations, introduction of foreign plant species and human influence. Natural fires and parasites may also contribute to shrinkage of forest area. Difficulty of human settlement or administrative decision might protect them from degradation. They can be set aside for hunting, logging, city climate control and tourism or general aesthetic enjoyment.</p>

    <p>There are many proposed classifications for forests. We will use the one used by the The UN’s Environment Programme’s World Conservation Monitoring Centre. That leaves us at:</p>

    <ul>
        <li>Temperate needleleaf;</li>
        <li>Temperate broadleaf and mixed;</li>
        <li>Tropical moist;</li>
        <li>Sparse trees and parkland;</li>
        <li>Forest plantations.</li>
    </ul>

