    <h1>13. The main mountains and volcanoes</h1>
    <h2>Introduction</h2>
    <p>Before we head on to the largest mountains in the world, we need to have a talk about measuring mountains.</p>

    <p>First of all, the most common way to measure a mountain is by its height, meaning elevation above mean sea level. However, a list of peaks by height is boring. Since the Himalayas are so high compared to all other mountain chains, you have to go a long way down to find a mountain from any other region. There are other ways to measure height, some more meaningful and interesting for us.</p>

    <p>There are many ways to measure mountains, more than a dozen. There's "isolation measure", "drop measure" and "spire measure", for example. We will follow for the most part "topographic prominence", which itself has many variances. There's "submarine prominence", "inverse prominence", "dominance" and "proportional prominence", for example.</p>

    <p>Each of these have different results. In "The Finest Peaks: Prominence and other Mountain Measures", for example, Adam Helman describes "spire measure": “attempts to quantify the sense of impressiveness afforded a mountain due to great angularity”. If this seems interesting, there's a list of the top peaks according to the reduced spire measure in the related section.</p>

    <p>So let’s take a moment to understand better the concept of topographic prominence. To start, please take a moment to understand the following image. The vertical lines show the prominence of the peaks.</p>

    <div class="around">

        <a id="image" href="https://upload.wikimedia.org/wikipedia/commons/2/22/Relative-height.svg" title="Topographic prominence. Source: Wikipedia."> <img class="fitImage" src="https://upload.wikimedia.org/wikipedia/commons/thumb/2/22/Relative-height.svg/500px-Relative-height.svg.png" alt="" /></a>

        <p><i><span class="centerText">Topographic prominence. Source: Wikipedia.</span></i></p>

    </div>

    <p>Topographic prominence is often defined as “the height of a mountain above the saddle of the highest ridge connecting it to a peak higher still”. You may understand prominence as a measure of the independence of a mountain. The largest mountain in an area will score high, "taking the credit" for the whole region. But neighboring peaks will score lower, since they depend on the elevated area for their high elevation. As a result, a list of mountains by prominence correlates better with their cultural presence: they stand out the most.</p>

    <p>That’s enough about this. Now we review a few interesting lists according to different measures, then move on to the mountains by prominence. After that, we will review mountains and volcanoes that are relevant for other reasons.</p>

