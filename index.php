    <p>All those <em>Homo sapiens</em> lived as nomadic hunter-gatherers all over the world. Their diet consisted mostly of scavenged vegetables—fruits, berries, nuts—and meat from hunting. It was a difficult and dangerous life, as people still did not have dominion over nature. The most important advancement from then on was the complexity of their brain. It allowed them to outperform every other predator and every other subspecies of human evolution. This upgrade came not from further evolution, but by the accumulation of experience and knowledge. With the development of Agriculture, societies became more complex and thus more powerful. Man becomes the wolf of man.</p>

    <p>At around 11,000 b.C, at the end of the last Ice Age, humans start to live in villages. Within a few thousand years, plant and animal domestication begins. As seen on the map above, by then humans had reached the Americas. They were still to reach Madagascar and New Zealand—the large islands next to Africa and Australia, respectively.</p>

    <p>Africa has so many large mammals today because they evolved together with humans, learning to fear them. By contrast, both in the Americas and in Australia, the arrival of humans meant the doom of native large mammals. For example, Australia had giant kangaroos and the Americas had giant ground sloths and mammoths.<p>

    <p>Wherever humans reached, they spread and multiplied fast. In a thousand years they populated the Americas.<p>

