    <p>The Indian Ocean lays between the Atlantic and the Pacific. It borders Africa to the West, the Middle East and Asia to the North, and Australia to the East. 16th century European explorers names it not in reference to the country of India, but to the East Indies. East Indies was the European name for East Asia and South East Asia. The West Indies would then be the Caribbean, on the other side of the Atlantic.</p>

