    <h2>The Eight-Thousanders</h2>
    <p>The eight-thousanders are the 14 mountain on Earth taller than 8,000 m, all in the Himalayas. In addition to being a nice round cut-off number, the figure is also relevant to climbers.</p>
    <p>The human body works best at sea-level. At an altitude of 2,100 m, we see the first signs of the effect of decreased atmospheric pressure on human blood. As you climb higher, pressure decreases exponentially, while temperature, only linearly. At the same time, montane climate itself becomes more extreme with height, due to the disturbing presence of the mountain itself. At 8,000 M high, the presence of oxygen in the air drops to one third of normal levels. Without supplementary oxygen, this causes a person to die in a few minutes. The body becomes weaker and the brain can’t function correctly, leading to bad decisions under stress. It was considered impossible to climb the Everest without supplementary oxygen, but Reinhold Messner disagreed.</p>
