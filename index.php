    </div>

    <h4>Sea caves</h4>

    <p>Sea caves are the result of wave erosion acting on coastal rock. The largest is Matainaka Cave, in New Zealand, which is 1.5 km long.</p>

    <div class="around">

        <a id="image" href="https://www.flickr.com/photos/mcosta1973/8913749007/" title="Blue Grotto, in Italy. Source: Flickr. Photographer: Michael Costa."> <img class="fitImage" src="https://farm9.staticflickr.com/8542/8913749007_a324789ee4_b.jpg" alt="" /></a>

