    <p>A drainage basin or catchment area is the area drained by a river. All humidity in that area flows into the river, be it originated from rainfall, tributary rivers or glaciers. In the comic, that would be the mountain range, as well as the area surrounding the tributary rivers and the main river itself. The forest area is not part of the drainage basin, as it seems to feed on rainfall from the Ocean humidity. This same humidity stops at the mountain range, giving origin to the desert. In the comic, two rivers flow into a lake, which then flows into the main river, which flows into the Ocean. This is also common.</p>

    <h3>Deltas and Estuaries</h3>

    <p>Upon reaching the ocean, a river might form a delta (as pictured on the xkcd comic) or an estuary. Though you will find those terms used without discretion as if interchangeable, there is a difference. Deltas are triagular shapes, as the river spreads on many smaller streams closer to the coast. They are fertile, because this river formation distributes its nutrient-rich sediment around. In estuaries, by contrast, rivers reach the ocean with more momentum. They drop straight into the ocean, affecting the landscape to a lesser degree.</p>

