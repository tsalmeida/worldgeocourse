    <h3>Biomes</h3>

    <p>Biomes are areas with similar conditions of climate, fauna and flora. A more popularized name for biome would be “ecosystem”. The whole planet is an ecosystem, as in a balanced ecological system, but regions may have a separate balance of their own. The interdependence between different elements may be surprising. Some trees are essential to their whole ecosystem, meanwhile being dependent on certain animals. At the same time, all are dependent on the climate and on the water cycle. Inside a biome you may have another biome of relative isolation, such as an oasis in a desert, or on mountains. The world climate is in constant change, both from natural and from man-made causes. Biomes expand and contract according to these changes. In the xkcd comic you will find three biomes: forest, plains and desert. Here is a world map of biomes:</p>

