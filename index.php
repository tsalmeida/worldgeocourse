    <h1>7. The main biomes</h1>

    <p>A biome or ecosystem is an area of similar climatic conditions and similar presence of life. The original life forms in a biome either evolved together or merged together at some moment. Excluding human influence, there is ecological balance between life population, geographical formations and climate. Introduced species may disturb this balance, but not always. In a similar way, humans may coexist without disturbing the ecosystem. The degree of disturbance varies according to culture and population size.</p>

    <p>Biomes follow from the climate found in the region. Below you will find a graph showing the biomes formed according to annual rainfall and average temperature.</p>

