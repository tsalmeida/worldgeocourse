    <h1>8. The main rivers</h1>

    <p>Rivers are essential to the understanding of human civilization and colonization of the planet. They are useful as a source of fresh water, transport and irrigation. Most exploration of the planet followed courses of rivers and when not present, human presence is small or absent. In history, rivers have been both pathways of war and natural barriers used in strategy. It’s no wonder many national borders follow the courses of rivers.</p>

    <p>The end result is that many major regional divisions of the planet follow from rivers. In turn, Regions are important for understanding the planet and a building block of Geography. For this reason, we will save the study of most rivers for the later part of the course, after this general introduction.</p>

    <p>In this chapter, we will review only the longest and most important rivers. In future chapters, we will delve deeper on them and the life that formed around them.</p>

    <h2>The Rivers of the Fertile Crescent</h2>

