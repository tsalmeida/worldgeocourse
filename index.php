    <h1>1. Up to the Cenozoic Era</h1>

    <p>First of all, the Geologic Time Scale is thus divided: Eon > Era > Period > Epoch > Age > Chron.</p>

    <h2>The Precambrian Supereon</h2>

    <p>The Precambrian “supereon” encompasses three eons: the Hadean, the Archean and the Proterozoic. As a result, it spams 88% of Earth's age, from its creation (4.6 billion years ago) to the explosion of life that took place in the Cambrian period (500 million years ago). The Cambrian Period is part of the Paleozoic Era and of the Phanerozoic Eon.</p>

    <p>In the Precambrian, the Earth’s magnetic poles drifted around the planet, causing glaciations even in areas that today are near the Equator. Also during the Precambrian early single-cell organisms generated the oxygen in our atmosphere. This oxygen not only fuels most of life on Earth, but also broke down creating ozone, which protects the planet from the Sun’s ultraviolet rays.</p>

    <h2>The Phanerozoic Eon</h2>

    <p>Why is the Cambrian period so important that geologists named everything before "Precambrian"? Because that is when the “Cambrian explosion” happened, an explosion of life on the planet. Before the Cambrian era, almost all life on Earth were micro-organisms that did not leave proper fossils. The first fossils found comes from the Cambrian period.</p>

    <div class="around">

        <a id="image" href="https://upload.wikimedia.org/wikipedia/commons/a/a2/Opabinia_BW2.jpg" title="An example of life in the Cambrian period. The above is an Opabinia regalis, about 10 centimeters long. Source: Wikipedia."> <img class="fitImage" src="https://upload.wikimedia.org/wikipedia/commons/a/a2/Opabinia_BW2.jpg" alt="" /></a>

