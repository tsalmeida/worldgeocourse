    <h2>Tectonic plates</h2>

    <p>Again, tectonic plate movement is the most powerful force shaping the planet. The map below shows where the tectonic plates are and in what direction they move.</p>

    <div class="around">

        <a id="image" href="https://upload.wikimedia.org/wikipedia/commons/8/8a/Plates_tect2_en.svg" title="Tectonic plates world map. Source: Wikipedia."> <img class="fitImage" src="https://upload.wikimedia.org/wikipedia/commons/thumb/8/8a/Plates_tect2_en.svg/800px-Plates_tect2_en.svg.png" alt="" /></a>

