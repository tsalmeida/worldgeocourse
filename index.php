    <p>The Caspian sea is the final destination of the water carried by the Volga river, the most important river of Russia. That means it is an “endorheic” drainage basin: unlike most others, it does not discharge into the ocean. How can the Caspian Sea receive the full discharge of the largest river in Europe, as well as 130 others? This is possible because it is so large that its rate of evaporation balances the water imput out. In fact, it is so large that, if considered a lake, it would be the largest in the world. To drive home how exceptional this is, remember that most other endorheic basins are deserts. Of the endorheic basins that do have rivers and lakes, the Volga/Caspian is the largest. However, thorugh man-made canals, today the Volga does connect to the Ocean.</p>

    <p>The Caspian stands in the border between Russia, Azerbaijan, Kazakhstan, Iran and Turkmenistan. The largest city on the Caspian is Baku, the capital of Azerbaijan. To the East of the Caspian, lies the Caucasus Mountains; to the west, Central Asia. Rich in oil, it was home to the first offshore oil wells, as well as the first machine-drilled oil well. In the early 20th century, Baku was the center of the oil industry. All this oil production was later administered by the Soviet Union, which captured the territory in 1920.</p>

    <p>Nearby Aral Sea does not look today as it did on the map above. Once one of the largest lakes in the world, and also endorheic, it has since dried-up due to use of its rivers for irrigation. The map below shows the shrinking of the Aral Sea up to 2008, but contemporary satellite images shows it even smaller.</p>

