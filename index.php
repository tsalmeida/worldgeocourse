    <h1>15. The climate</h1>

    <p>This is a crash course in basic Climatology, the science charged with studying the climate.</p>

    <p>The distinction between climate and weather is fundamental. Climate refers to long term patterns, while weather is the climate condition in a short period—a day, for example.</p>

    <p>The single most important factor to the climate is the Sun and how it doesn’t shine in the entire planet the same way. In the image below, you see that the Earth is tilted as it circles around the Sun. However it does also spin. As a result, there is a large region near the Equator that faces straight at the Sun. The regions far from the Equator face the Sun from an angle, so they get less sunlight.</p>

    <div class="around">

        <a id="image" href="https://upload.wikimedia.org/wikipedia/commons/6/61/AxialTiltObliquity.png" title="Earth's axial tilt. Source: Wikipedia."> <img class="fitImage" src="https://upload.wikimedia.org/wikipedia/commons/6/61/AxialTiltObliquity.png" alt="" /></a>

        <p><i><span class="centerText">Earth's axial tilt. Source: Wikipedia.</span></i></p>

    </div>

    <p>The following image shows the intertropical convergence zone between the tropics: Cancer and Capricorn. From the tropics to the arctic and antarctic circles, we have the temperate zones. From the arctic and antarctic circles to the poles, we see the frigid zones. Insulation is different for each of these zones.</p>

    <div class="around">

        <a id="image" href="https://upload.wikimedia.org/wikipedia/commons/b/b0/World_map_indicating_tropics_and_subtropics.png" title="Intertropical zone. Source: Wikipedia."> <img class="fitImage" src="https://upload.wikimedia.org/wikipedia/commons/thumb/b/b0/World_map_indicating_tropics_and_subtropics.png/800px-World_map_indicating_tropics_and_subtropics.png" alt="" /></a>

        <p><i><span class="centerText">Intertropical zone. Source: Wikipedia.</span></i></p>

    </div>

    <p>The Earth does not rotate around the Sun in a circle, but in an ellipses. Because of this, the Sun is closer in some parts of the year than in others. In the temperate zone, where sunlight is already lower, that is enough to cause the seasons. Around the Equator, always facing squarely at the Sun, the difference isn’t as great and there are no seasons. However, since the global climate is one entire system, it also faces changes: a rainy season and a dry season.</p>

    <h2>The Atmosphere</h2>

    <p>On the image below, you will see the layers of the atmosphere. Only the troposphere and the stratosphere are of concern to us.</p>

    <div class="around">

        <a id="image" href="https://upload.wikimedia.org/wikipedia/commons/f/f9/Atmosphere_layers-en.svg" title="The layers of the Atmosphere. Source: Wikipedia."> <img class="fitImage" src="https://upload.wikimedia.org/wikipedia/commons/thumb/f/f9/Atmosphere_layers-en.svg/102px-Atmosphere_layers-en.svg.png" alt="" /></a>

        <p><i><span class="centerText">The layers of the Atmosphere. Source: Wikipedia.</span></i></p>

    </div>

    <p>Most human activity, except for high-altitude aviation and spaceflight, happens in the troposphere. The stratosphere is relevant to us for two reasons: it is home to the jet stream and to the ozone layer.</p>

    <h3>Composition</h3>

    <p>Earth’s atmosphere consists roughly of:</p>

    <ul>
        <li>78% nitrogen;</li>
        <li>21% oxygen;</li>
        <li>1% other gases.</li>
    </ul>

    <p>Both Nitrogen and Oxygen are abundant because they are heavy enough not to escape the atmosphere. Volcanic eruptions expelled the nitrogen gas and living organisms created the oxygen, over billions of years through photosynthesis. The 1% of other gases includes ozone, carbon dioxide, water and others. Both ozone and carbon dioxide are greenhouse gases, they help keep heat from the Sun in the planet surface. Without the greenhouse effect Earth would freeze, being unable to hold heat generated by the Sun. With a stronger greenhouse effect, the planet would become warmer. Photosynthesis, the origin of all the oxygen, uses carbon dioxide. In turn, it protects the same living organisms from genetic damage by ultraviolet from the Sun.</p>

    <h2>Atmospheric Circulation</h2>

    <p>Alright, so it’s warmer in the Equator, it’s a little colder in the tropics and much colder in the poles. All that differently-heated air doesn’t just stay where it is. This is the most important knowledge you have to take out of this chapter. Everything that happens next in the climate follows from this difference in temperature: the result is "atmospheric circulation".</p>

    <p>Atmospheric circulation is the movement of air throughout the atmosphere of the planet. It follows from the second law of thermodynamics, as the different sections of the planet exchange heat. Without the input of more heat from the Sun, this would result in entropy: the entire atmosphere would end with the same temperature. However, because the Sun heats the Equator more than the tropics and the poles every day, the process never stops.</p>

    <p>Moving air is how atmospheric heat gets exchanged around: that's wind. The warm winds of the Equator blow to the tropics. The medium-warm winds of the temperate zone blow to the Equator and to the poles. The cold winds of the poles blow to the tropics or in a spiral around the poles, trapped. The result is what you see below.</p>

    <div class="around">

        <a id="image" href="https://upload.wikimedia.org/wikipedia/commons/c/cd/AtmosphCirc2.png" title="Idealized atmospheric circulation. Source: Wikipedia."> <img class="fitImage" src="https://upload.wikimedia.org/wikipedia/commons/c/cd/AtmosphCirc2.png" alt="" /></a>

        <p><i><span class="centerText">Idealized atmospheric circulation. Source: Wikipedia.</span></i></p>

    </div>

    <p>So, we have one “cell” for each of the zones mentioned before. The Hadley Cell around the Equator, the Ferrel Cell between the tropics and the poles and the Polar Cell in the poles. On the image, the blue arrows are cold winds, the red arrows are warm winds.</p>

    <p>Before we go into more detail, please notice the directions of surface wind. Around the Equator, surface wind blows West and toward the Equator. In the Ferrel Cell, surface wind blows East and toward the poles. In the poles, surface wind blows West and toward the tropics. The words in yellow are the names of each of those winds: trade winds, westerlies and polar easterlies. However, in reality winds don’t follow exactly that pattern because they function differently over land and sea. We will see a “real” wind map further below.</p>

    <p>You may notice that as well as moving across the planet, winds also move vertically. This happens because cold air is heavier than hot air. As the hot air moves upward, however, it also cools down, becoming cold air. It then moves downward, as hot air takes its place. The Hadley Cell is so tall because warm winds meet in the Equator and there’s only one way from there: up.</p>

    <p>There is one other wind movement we can observe. We have accounted for vertical movement and for movement across the planet. The latter doesn’t happen in a straight line though, and we haven’t accounted for that. The cold winds in the Hadley Cell move from east to west due to the Coriolis effect. As the Earth spins around its axis east to west, it drags the atmosphere, but not as much as the land. So a wind moving north while the Earth spins, when plotted on a stationary map, will seem to curve. Read that again, take a look at the map, look at the image below and try to wrap your mind around it.</p>

    <div class="around">

        <a id="image" href="https://upload.wikimedia.org/wikipedia/commons/b/b6/Corioliskraftanimation.gif" title="Representation of the Coriolis Effect. Source: Wikipedia."> <img class="fitImage" src="https://upload.wikimedia.org/wikipedia/commons/b/b6/Corioliskraftanimation.gif" alt="" /></a>

        <p><i><span class="centerText">Representation of the Coriolis Effect. Source: Wikipedia.</span></i></p>

    </div>

    <p>Now that we have understood what traces the path of winds, let’s go through each of the three cells.</p>

    <h3>The Hadley Cell</h3>

    <p>Please take a moment to observe the Hadley Cell in the image from before.</p>

    <p>As the sun heats the surface near the Equator, the air becomes hotter and rises, carrying humidity. It is then replaced by cold and dry air from the higher troposphere, which proceeds to warm up and gain humidity near the surface. Meanwhile, the hot and humid air forms rain clouds and, with altitude, becomes colder and rains down, losing humidity and mass. The now lost water in the air stored most of the heat, so the air becomes cold and dry again, so it falls back down to the surface. The end result is clear: heavy rainfall in the intertropical convergence zone, which the Hadley Cell covers. The word “Convergence” comes from the northeast and the southeast trade winds coming together.</p>

    <h3>The Polar Cell</h3>

    <p>Now let’s compare that to what happens in the poles. Because there isn’t as much heat from the Sun in the poles, the air is cold and dry. However, the air at the Arctic and Antarctic circles is still warmer than the air right on the poles, since it is in constant exchange with the warmer winds of the Ferrel Cell. This difference of temperature causes this warmer air to go toward the pole, where it rapidly cools off again. As a result, the poles function as a heat sink: whatever heated and humid air comes in contact with it, becomes cold and dry. Because it is so cold in the poles, the humidity does not fall as rain, but as snow. This sets the stage for the polar deserts we mentioned in the biomes chapter.</p>

    <h3>The Ferrel Cell</h3>

    <p>Now we circle back to analyze the Ferrel Cell, between the tropics and the arctic and antarctic circles. The Ferrel Cell receives both cold and dry air from the poles and warm and humid air from the intertropical convergence zone. The question is, which wins, that is, which is stronger? The answer is, it depends on the time of the year. When the Earth is closer to the Sun—during Summer—the warm winds of the Equator dominate. But when the Earth is further away from the Sun—during Winter— the cold winds of the poles expand and escape.</p>

    <p>In the Ferrel Cell region, we find the westerlies winds.</p>

