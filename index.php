    <h3>Mountains and volcanoes</h3>

    <p>A volcano is a type of a mountain, as in "large and tall landforms", but both result from tectonic plate movement. </p>

    <p>Tectonic plate movement is the most powerful force shaping the planet. Where tectonic plates clash, either one or both of them climb, expelling underground material. If the tectonic plates move away from each other, leaving the underground unexposed, the same happens. The volcano is the conduit of this exposure. One volcanic eruption may expel magma, volcanic ash, gases or combinations of them.</p>

    <p>Here's a photograph of an erupting volcano:</p>

