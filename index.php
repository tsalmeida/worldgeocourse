    <p>Monsoon forests are characteristic of areas subject to monsoon rains, which we will review in a later chapter. These areas have a long dry season followed by a season of heavy rainfall. Some plants shed their leaves during the dry season. We find this biome in Southeast Asia and one typical plant is the bamboo.</p>

    <h2>Arid Desert</h2>

    <div class="around">

        <a id="image" href="https://upload.wikimedia.org/wikipedia/commons/3/34/Rub_al_Khali_002.JPG" title="Arid Desert in Saudi Arabia. Source: Wikipedia."> <img class="fitImage" src="https://upload.wikimedia.org/wikipedia/commons/thumb/3/34/Rub_al_Khali_002.JPG/800px-Rub_al_Khali_002.JPG" alt="" /></a>

        <p><i><span class="centerText">Arid Desert in Saudi Arabia. Source: Wikipedia.</span></i></p>

    </div>

    <p>Arid regions have a severe lack of humidity, which in turn causes a lack of nutrients, preventing animal and plant life. Because air humidity is low, there are extreme variations in temperature between night and day. Coupled with the lack of plant life to protect the soil, this results in further degradation of rock and nutrients. Nearby mountain ranges are the ultimate cause for some deserts, as they force clouds upwards. In these cases, rain falls on one side of the mountain chain, but not on the other, which then becomes a desert.</p>

    <div class="around">

        <a id="image" href="https://upload.wikimedia.org/wikipedia/commons/6/66/Agasthiyamalai_range_and_Tirunelveli_rainshadow.jpg" title="The hills on the background cut off this region in India from the monsoon rains. Source: Wikipedia."> <img class="fitImage" src="https://upload.wikimedia.org/wikipedia/commons/thumb/6/66/Agasthiyamalai_range_and_Tirunelveli_rainshadow.jpg/800px-Agasthiyamalai_range_and_Tirunelveli_rainshadow.jpg" alt="" /></a>

        <p><i><span class="centerText">The hills on the background cut off this region in India from the monsoon rains. Source: Wikipedia.</span></i></p>

    </div>

    <p>Water presence, such as from a passing river carrying nutrient-rich sediment, eases this condition. Oases are another source of water, resulting from springs and outflows of underground water. The result is vegetation and animal life present in the margins of rivers or oases.</p>

    <div class="around">

        <a id="image" href="https://upload.wikimedia.org/wikipedia/commons/f/f5/Oasis_in_Libya.jpg" title="An Oasis in Lybia. Source: Wikipedia."> <img class="fitImage" src="https://upload.wikimedia.org/wikipedia/commons/thumb/f/f5/Oasis_in_Libya.jpg/800px-Oasis_in_Libya.jpg" alt="" /></a>

        <p><i><span class="centerText">An Oasis in Lybia. Source: Wikipedia.</span></i></p>

