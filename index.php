    <h3>The Cretaceous-Paleogene Extinction Event</h3>

    <p>The dinosaurs lived in the Cretaceous Period. This period ends with the “Cretaceous-Paleogene extinction event”, 66 million years ago, giving rise to the Paleogene Period. That event caused the extinction of three of every four species of plants and animals in the planet. Its cause was the impact of a meteor in the Yucatán Peninsula, in today’s Mexico, near the Caribbean, where it left a large crater. The impact was so great that the oxygen in the atmosphere caught fire for a brief time, incinerating the surface of the planet. Giant firestorms, which lasted longer, where also created and caused further destruction. The picture below, taken in Canada, shows a natural record of this.</p>

    <div class="around">

        <a id="image" href="https://upload.wikimedia.org/wikipedia/commons/0/06/KT_boundary_054.jpg" title="An example of an exposed ’KT Boundary’, as in the boundary between the Cretaceous and the Paleogene. The section below the dark line was covered when the incineration of the planet happened. Source: Wikipedia."> <img class="fitImage" src="https://upload.wikimedia.org/wikipedia/commons/thumb/0/06/KT_boundary_054.jpg/800px-KT_boundary_054.jpg" alt="" /></a>

        <p><i><span class="centerText">An example of an exposed “KT Boundary”, as in the boundary between the Cretaceous and the Paleogene. The rock below the dark line was covered when the incineration of the planet happened. Source: Wikipedia.</span></i></p>

    </div>

    <p>The impact also caused longer-lasting climate change ("impact winter"), a rise of the sea level and mega tsunamis. The raw power of the impact is similar to a billion times that of the atomic bombs used in World War II.</p>

    <p>Only small animals that burrow underground or hide in caves, such as mammals, were able to survive. This allowed them to dominate life on the Planet for the next millions of year, including today. Mammal species grew 10% larger in size in the following millions of years.</p>

    <h3>The Cenozoic Era</h3>

    <p>We live in the Cenozoic Era, nicknamed “The Age of Mammals”, which started 66 million years ago.</p>

    <div class="around">

        <a id="image" href="https://upload.wikimedia.org/wikipedia/commons/5/53/Miocene.jpg" title="An example of life in the early Cenozoic Era. Source: Wikipedia."> <img class="fitImage" src="https://upload.wikimedia.org/wikipedia/commons/thumb/5/53/Miocene.jpg/575px-Miocene.jpg" alt="" /></a>

        <p><i><span class="centerText">An example of life in the early Cenozoic Era. Source: Wikipedia.</span></i></p>

    </div>

    <p>During the Cenozoic Era, the continents assume the shape we currently know. Then happens the creation of the Isthmus of Panama, one of the most important geological events in the history of the planet. Connecting North and South America, it is today occupied by the country of Panama, home to the Panama Canal. Its effect of cutting-off the connection between the Atlantic and Pacific oceans had great effect in the climate of the planet. We will study its importance in a future chapter.</p>

